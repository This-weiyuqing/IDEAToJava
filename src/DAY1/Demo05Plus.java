package DAY1;

public class Demo05Plus {
    public static void main(String[] args) {
        //加号
        //用加号相连都会变成string
        String str1="hello";
        System.out.println(str1);
        System.out.println("hello"+"world");
        String str2="java";
        System.out.println(str2+20);
        System.out.println(str2+20+30);
        System.out.println(str2+(20+30));
    }
}
