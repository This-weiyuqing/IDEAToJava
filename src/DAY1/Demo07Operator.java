package DAY1;

/*赋值运算符

  =  赋值
  +=    a+=3    a=a+3
  -=    b-=4    b=b-4
  *=    c*=5    c=c*5
  /=    d/=6    d=d/6
  %=    e%=7    e=e%7
  */
public class Demo07Operator {
    public static void main(String[] args) {
        int a=10;
        a+=5;
        System.out.println(a);
        int x=10;
        x%=3;
        System.out.println(x);
    }
}
